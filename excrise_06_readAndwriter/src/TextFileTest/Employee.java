package TextFileTest;

public class Employee {
	private String name;
	private int salary;
	
	public Employee(String name, int Salary){
		this.name = name;
		this.salary = Salary;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getSalary() {
		return salary;
	}
	public void setSalary(int salary) {
		salary = salary;
	}
	
}
